export default class LeaderBoardAdapter {
  private _players: Array<IEntity>;
  constructor(players: Array<IEntity>) {
    this._players = players;
  }

  public getLeaderBoardData(): ILeaderBoard {
    // need to return only first 10 players
    const scores = this._players.map((x) => {
      return x.size;
    });

    const leaderBoard: ILeaderBoard = {
      scores,
    };

    return leaderBoard;
  }
}
